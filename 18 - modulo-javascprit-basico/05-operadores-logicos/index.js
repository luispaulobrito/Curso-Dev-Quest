/*
    == compara se o conteúdo é igual
    === compara se o conteúdo e o tipo da variável são iguais
    != não igual
    !== não idêntico
    && = e
    || = ou
    ! = negação
*/

const a = 3
const b = 3

console.log(a==b)
console.log(a===b)
console.log(a===b && a>b)
console.log(a===b || a>b)
console.log(!(a===b))
